// ============================
// Form-ready Select Component
// ============================
export interface SelectProps {
  /**
   * Label text displayed above the select field
   */
  label?: string;
  /**
   * Current selected value
   */
  value?: string;
  /**
   * Callback function called when selection changes
   * @param value - The selected option value
   */
  onValueChange?: (value: string) => void;
  /**
   * Array of options to display in the select
   */
  options?: SelectOption[];
  /**
   * Placeholder text when no option is selected
   */
  placeholder?: string;
  /**
   * Error message to display below the select
   */
  error?: string;
  /**
   * Whether the field is required (shows * in label)
   */
  required?: boolean;
  /**
   * Helper text displayed below the select
   */
  helperText?: string;
  /**
   * When true, select is disabled
   */
  disabled?: boolean;
  /**
   * Custom CSS classes for the container
   */
  className?: string;
}

const Select = React.forwardRef<
  HTMLDivElement,
  SelectProps
>(({
  label,
  value,
  onValueChange,
  options = [],
  placeholder = 'Selecciona una opciÃ³n',
  error,
  required = false,
  helperText,
  disabled = false,
  className,
}, ref) => {
  
  const hasError = !!error;
  const hasHelperText = !!helperText && !hasError;

  return (
    <div ref={ref} className={cn('space-y-2', className)}>
      {/* Label */}
      {label && (
        <label className={cn(
          'text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70',
          hasError ? 'text-destructive' : 'text-foreground'
        )}>
          {label}
          {required && <span className="text-destructive ml-1">*</span>}
        </label>
      )}

      {/* Select */}
      <SelectRoot value={value} onValueChange={onValueChange} disabled={disabled}>
        <SelectTrigger className={cn(
          hasError && 'border-destructive focus:ring-destructive'
        )}>
          <SelectValue placeholder={placeholder} />
        </SelectTrigger>
        <SelectContent>
          {options.map((option) => (
            <SelectItem 
              key={option.value} 
              value={option.value}
              disabled={option.disabled}
            >
              {option.label}
            </SelectItem>
          ))}
        </SelectContent>
      </SelectRoot>

      {/* Error Message */}
      {hasError && (
        <p className="text-sm text-destructive font-medium" role="alert">
          {error}
        </p>
      )}

      {/* Helper Text */}
      {hasHelperText && (
        <p className="text-sm text-muted-foreground">
          {helperText}
        </p>
      )}
    </div>
  );
});

Select.displayName = 'Select';

